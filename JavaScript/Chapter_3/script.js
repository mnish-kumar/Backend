// Operator in JavaScript

// Arithmetic Operators
let a= 10;
let b = 10;
console.log("Addition:", a + b); // Addition
console.log("Subtraction:", a - b); // Subtraction
console.log("Multiplication:", a * b); // Multiplication
console.log("Division:", a / b); // Division
console.log("Modulus:", a % b); // Modulus
console.log("Exponentiation:", a ** b); // Exponentiation

// Comparison Operators

let x = 5;
let y = 10;
console.log("Equal:", x == y); // Equal
console.log("Not Equal:", x != y); // Not Equal
console.log("Strict Equal:", x === y); // Strict Equal
console.log("Strict Not Equal:", x !== y); // Strict Not Equal
console.log("Greater Than:", x > y); // Greater Than
console.log("Less Than:", x < y); // Less Than
console.log("Greater Than or Equal:", x >= y); // Greater Than or Equal
console.log("Less Than or Equal:", x <= y); // Less Than or Equal


// Logical Operators
let p = true;
let q = false;

console.log("Logical AND:", p && q); // Logical AND

console.log("Logical OR:", p || q); // Logical OR

console.log("Logical NOT:", !p); // Logical NOT


// Unary Operators
let num = 5;

console.log("Unary Plus:", +num); // Unary Plus

console.log("Unary Negation:", -num); // Unary Negation

console.log("Increment:", ++num); // Increment

console.log("Decrement:", --num); // Decrement


// Terary Operator
let condition = true;

let result = condition ? "Condition is true" : "Condition is false";

console.log("Ternary Operator Result:", result); // Ternary Operator Result

